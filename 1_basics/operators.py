# Arithmatic operators

# Operators like  + , - , * , / , %
a = 7
b = 10

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b) # remainder
print(a**b) # a^b

# Relational operators

c = 50
d = 20
print(c==d) # False
print(c!=d) # True
print(c>=d) # True
print(c>d) # True
print(c<d) # False
print(c<=d) # False

# Assignment operators
num = 10
